#What does each method return in the following example?

arr = [15, 7, 18, 5, 12, 8, 5, 1]

arr.index(5) # will return index position of 3 since it is the first index that matches the value of the argument
arr.index[5] # will return an error because square brackets can't be used with the index method
arr[5] #will return a value of 8 from index position 5